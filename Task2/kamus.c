void draw_dot(int x, int y, color* c){
	if((x<1) || (x>layarx)){
		return;
	}

    long int position = (x + var_info.xoffset) * (var_info.bits_per_pixel / 8) + (y + var_info.yoffset) * fix_info.line_length;
    if(var_info.bits_per_pixel == 32){
        *(fb + position) = c->b;
        *(fb + position + 1) = c->g;
        *(fb + position + 2) = c->r;
        *(fb + position + 3) = c->a;
    }else{
        int b = c->b;
        int g = c->g;
        int r = c->r;
        unsigned short int t = r<<11 | g << 5 | b;
        *((unsigned short int*)(fb + position)) = t;
    }
}

void draw_garis(int x0, int x1, int y0, int y1, color* c) {
	int xtemp = x1 - x0;
	int xincr = 1;
	if (xtemp < 0) {
		xtemp *= -1;
		xincr = -1;
	}
	
	int ytemp = y1 - y0;
	int yincr = 1;
	if (ytemp < 0) {
		ytemp *= -1;
		yincr = -1;
	}

	if (xtemp == ytemp) {
		while (xtemp >= 0) {
			draw_dot(x0,y0,c);
			x0 += xincr;
			y0 += yincr;			
			xtemp--;
		}
	} else if (xtemp < ytemp) {
		int selisih = ytemp - xtemp;
		int incr = 0;
		while (ytemp >= 0) {
			draw_dot(x0,y0,c);
			ytemp--;
			incr++;
			y0 += yincr;
			if (incr == selisih) {
				x0 += xincr;
				incr = 0;
			}
		}
	} else {
		int selisih = xtemp - ytemp;
		int incr = 0;
		while (xtemp >= 0) {
			draw_dot(x0,y0,c);
			xtemp--;
			incr++;
			x0 += xincr;
			if (incr == selisih) {
				y0 += yincr;
				incr = 0;
			}
		}
	}
}

