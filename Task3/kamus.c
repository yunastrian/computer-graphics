int isAnomali(int x, int y){
  int anomali=0;
  int cb1=0 , cb2=0;
  if (buffer_r[x-1][y-1] != 0){
    cb1++;
  }
  if (buffer_r[x][y-1] != 0){
    cb1++;
  }
  if (buffer_r[x+1][y-1] != 0){
    cb1++;
  }
  if (buffer_r[x-1][y+1] != 0){
    cb2++;
  }
  if (buffer_r[x][y+1] != 0){
    cb2++;
  }
  if (buffer_r[x+1][y+1] != 0){
    cb2++;
  }
  if ((cb1 == 2) || (cb2 == 2)) {
    anomali = 1;
  } else{
    anomali = 0;
  }
  return anomali;
}


